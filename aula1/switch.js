let numero = 13;

switch ( numero ) {
    case 13:
        console.log( '13 - Lula' );
        break;
    case 22:
        console.log( '22 - Bolsonaro' );
        break;
    case 50:
        console.log( '50 - Ciro' );
        break;
    case 21:
        console.log( '21 - Simone Tebet' );
        break;
    default:
        console.log( 'Número inválido' );
        break;
}